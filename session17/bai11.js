// Sử dụng Math.abs() - Trả về giá trị tuyệt đối của một số
console.log(Math.abs(-5.5)); // Kết quả: 5.5 (loại bỏ dấu âm)
console.log(Math.abs(10));   // Kết quả: 10 (số dương giữ nguyên)

// Sử dụng Math.ceil() - Làm tròn lên đến số nguyên gần nhất
console.log(Math.ceil(4.4)); // Kết quả: 5 (4.4 làm tròn lên thành 5)
console.log(Math.ceil(5.8)); // Kết quả: 6 (5.8 làm tròn lên thành 6)

// Sử dụng Math.floor() - Làm tròn xuống đến số nguyên gần nhất
console.log(Math.floor(5.7)); // Kết quả: 5 (5.7 làm tròn xuống thành 5)
console.log(Math.floor(6.1)); // Kết quả: 6 (6.1 làm tròn xuống thành 6)

// Sử dụng Math.round() - Làm tròn đến số nguyên gần nhất
console.log(Math.round(10.1)); // Kết quả: 10 (10.1 gần 10 hơn 11 nên làm tròn xuống)
console.log(Math.round(20.6)); // Kết quả: 21 (20.6 gần 21 hơn 20 nên làm tròn lên)